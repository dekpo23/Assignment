#Prompt user for age and store in age variable
age = int(input('Enter your age here: '))

#Prompt user for height and store in height variable
height = float(input('Enter your height(in metres) here: '))

#Prompt user for name and store in name variable
name = input('Enter your name here: ')

#Print final sentence using f string
print(f'{name} is {age} years old and {height} metres tall.')

